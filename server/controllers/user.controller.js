import asyncHandler from 'express-async-handler';
import { OAuth2Client } from "google-auth-library";
import jwt from 'jsonwebtoken';
import bcrypt from "bcrypt"
import {User} from '../models/user.models.js';


const generateToken = (user) => {
  return jwt.sign(
    { id: user._id, email: user.email },
    process.env.JWT_SECRET,
    { expiresIn: '9999d' }
  );
};


const client = new OAuth2Client(process.env.GOOGLE_CLIENT_ID);



// Google Login
export const googleLogin = async (req, res) => {
  try {
    const { token } = req.body; // frontend will send Google ID token

    if (!token) {
      return res.status(400).json({ msg: "No token provided" });
    }

    // Verify token with Google
    const ticket = await client.verifyIdToken({
      idToken: token,
      audience: process.env.GOOGLE_CLIENT_ID,
    });

    const payload = ticket.getPayload();
    const { email, name, picture, sub: googleId } = payload;

    // Check if user already exists
    let user = await User.findOne({ email });

    if (!user) {
      // Create new user if not exists
      user = new User({
        username: name.toLowerCase().replace(/\s+/g, "_"),
        email,
        provider: "google",
        avatar: picture,
        password: null, // no password for google users
      });
      await user.save();
    }

    // Generate JWT
    const jwtToken = generateToken(user);

    res.json({
      success: true,
      token: jwtToken,
      user: {
        id: user._id,
        username: user.username,
        email: user.email,
        avatar: user.avatar,
        provider: user.provider,
      },
    });
  } catch (err) {
    console.error("Google login error:", err);
    res.status(500).json({ msg: "Google login failed" });
  }
};


// auth.controller.js
export const handleGoogleAuth = asyncHandler(async (req, res) => {
  if (!req.user) {
    res.status(401);
    throw new Error('Google authentication failed');
  }

  const token = generateToken(req.user);
  const userData = {
    _id: req.user._id,
    name: req.user.name,
    email: req.user.email,
    avatar: req.user.avatar,
    provider: req.user.provider
  };

  // For API responses
  res.status(200).json({
    success: true,
    ...userData,
    token
  });
});





export const registerAccount = asyncHandler(async (req, res) => {
  const { fullname, username, email, password } = req.body;

  // 1️⃣ Validate required fields
  if (!fullname || !username || !email || !password) {
    res.status(400);
    throw new Error("Full name, username, email, and password are required");
  }

  // 2️⃣ Check password length
  if (password.length < 8) {
    res.status(400);
    throw new Error("Password must be at least 8 characters long");
  }

  // 3️⃣ Check for existing user (email or username)
  const existingUser = await User.findOne({
    $or: [
      { email: email.toLowerCase().trim() },
      { fullname: fullname.toLowerCase().trim() },
      
    ]
  });
  if (existingUser) {
    res.status(400);
    throw new Error("User already exists");
  }

  // 4️⃣ Create new user (hash password)
  const hashedPassword = await bcrypt.hash(password, 10);
  const user = new User({
    fullname: fullname.toLowerCase().trim(),
    username: username.toLowerCase().trim(),
    email: email.toLowerCase().trim(),
    password: hashedPassword,
    provider: 'local'
    // avatar auto-generated by your pre-save hook
  });

  await user.save();

  // 5️⃣ Return success response
  res.status(201).json({
    success: true,
    message: "Account created successfully",
    user: {
      id: user._id,
      fullname: user.fullname,
      username: user.username,
      email: user.email,
      provider: user.provider,
      avatar: user.avatar
    }
  });
});


export const loginAccount = asyncHandler(async (req, res) => {
  const { username, password } = req.body;

  // Validate input
  if (!username || !password) {
    return res.status(400).json({ msg: "username and password are required" });
  }

  // Find user by email
  const user = await User.findOne({ username }).select("+password");
  if (!user) {
    return res.status(401).json({ msg: "User not found" });
  }

  // Compare password
  const isPasswordCorrect = await bcrypt.compare(password, user.password || "");
  if (!isPasswordCorrect) {
    return res.status(401).json({ msg: "Invalid credentials" });
  }

  // Generate token
  const token = generateToken(user);

  // Remove password from response
  const { password: _, ...userData } = user.toObject();

  return res.status(200).json({
    msg: "Login successful",
    token,
      user: {
    ...userData,
    avatar: user.avatar // Include the avatar in response
  },
  });
});

